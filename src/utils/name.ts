export const getName = (nodeName: string) => {
  switch (nodeName.toUpperCase()) {
    case 'DIV':
    case 'SPAN':
    case 'LABEL':
      return '编组';
    case 'TABLE':
      return '表格';
    case 'BUTTON':
      return '按钮';
    case 'TH':
      return '表头';
    case 'TR':
      return '行';
    case 'TD':
      return '单元格';
    case 'TBODY':
      return '表内容';
    case 'INPUT':
      return '输入框';
    default:
      return nodeName.toString();
  }
};
